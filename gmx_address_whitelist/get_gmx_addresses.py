OUTPUT_FILE = "observed_addresses.json"
PROGRESS_FILE = "progress.json"
 
lock = Lock()
 
def load_progress():
    if os.path.exists(PROGRESS_FILE):
        with open(PROGRESS_FILE, "r") as f:
            return json.load(f)
    return {"done_addresses": [], "observed": []}
 
def save_progress(progress):
    with lock:
        with open(PROGRESS_FILE, "w") as f:
            json.dump(progress, f, indent=2)
 
def save_final(observed):
    with open(OUTPUT_FILE, "w") as f:
        json.dump(sorted(list(observed)), f, indent=2)
 
def get_tx_list(address):
    url = f"https://api.etherscan.io/v2/api"
    params = {
        "module": "account",
        "action": "txlist",
        "address": address,
        "startblock": 0,
        "endblock": 99999999,
        "page": 1,
        "offset": 100,
        "sort": "desc",
        "apikey": ARBISCAN_API_KEY,
        "chainId": 421614
    }
    r = requests.get(url, params=params)
    data = r.json()
    if data["status"] != "1":
        return []
    return data["result"]
 
def get_receipt(tx_hash):
    payload = {
        "jsonrpc": "2.0",
        "method": "eth_getTransactionReceipt",
        "params": [tx_hash],
        "id": 1
    }
    r = requests.post(RPC_URL, json=payload)
    return r.json()["result"]
 
def main():
    progress = load_progress()
    observed = set(progress["observed"])
    done = set(progress["done_addresses"])
 
    for addr in SEED_ADDRESSES:
        if addr in done:
            continue
 
        print(f"Processing {addr}...")
 
        txs = get_tx_list(addr)
        print(f"  Found {len(txs)} txs")
 
        for tx in txs:
            tx_hash = tx["hash"]
 
            try:
                receipt = get_receipt(tx_hash)
                if receipt and "logs" in receipt:
                    for log in receipt["logs"]:
                        observed.add(log["address"].lower())
            except Exception as e:
                print(f"Error fetching receipt: {e}")
 
            time.sleep(0.2)  # RPC throttle
 
        done.add(addr)
 
        # Save progress frequently
        progress = {
            "done_addresses": list(done),
            "observed": list(observed)
        }
        save_progress(progress)
 
        time.sleep(0.5)  # Arbiscan throttle
        print(f"Observed so far: {len(observed)} addresses")
 
    save_final(observed)
    print("Done. Observed addresses saved.")
 
if __name__ == "__main__":
    main()

